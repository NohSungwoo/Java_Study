package variable.ex;

public class VarEx3 {
    public static void main(String[] args) {
        /*
        long 타입의 변수를 선언하고,
        그 변수를 10,000,000,000(백억)으로 초기화한 후 출력하는 프로그램을 작성하세요.
        boolean 타입의 변수를 선언하고, 그 변수를 true로 초기화한 후 출력하는 프로그램을 작성하세요.
         */

        long l;
        l = 10000000000L;

        boolean b;
        b= true;

        System.out.println(l);
        System.out.println(b);
    }
}
