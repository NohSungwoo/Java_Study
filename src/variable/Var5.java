package variable;

public class Var5 {
    public static void main(String[] args) {

        int a = 100; // 정수
        double b = 1.5; // 실수
        boolean c = true; // boolean
        char d = 'A'; // 문자
        String e = "Hello"; // 문자열

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
